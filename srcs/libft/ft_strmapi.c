#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int unsigned	i;
	char			*str;

	i = 0;
	str = (char *)malloc(ft_strlen(s) + 1 * (sizeof(char)));
	if (!s || !str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
